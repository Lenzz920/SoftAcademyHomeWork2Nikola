package loops.homework;

public class Problem_3_Print_Numbers_With_Step {

    public static void main(String[] args) {

        int from = 0;
        int to = 100;

        for(int i = from; i <= to; i+= 5) {
            System.out.println(i);
        }

        System.out.println("\nProgram terminated");

    }

}
